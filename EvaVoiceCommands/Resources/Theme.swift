import UIKit

class Theme {
    
    class func primaryOrangeColor() -> UIColor {
        return UIColor(red: 250.0/255.0, green: 86.0/255.0, blue: 63.0/255.0, alpha: 1.0)
    }
    
    
    class func dimPrimaryOrangeColor() -> UIColor {
        return UIColor(red: 204.0/255.0, green: 68.0/255.0, blue: 50.0/255.0, alpha: 1.0)
    }
    
    
}

